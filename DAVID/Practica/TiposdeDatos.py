#Tipos de datos enetros (int) y largo (long)
numeroEntero = 9 
print ("El valor de la variable numeroEntero es:",numeroEntero, "y su tipo de dato es", type (numeroEntero))
numeroLargo = 9
print ("El valor de la variable numeroLargo es:",numeroLargo, "y su tipo de dato es", type (numeroLargo))

#Tipos de datos float
numeroFlotante = 5.66
print(numeroFlotante, type (numeroFlotante))

#Tipo de datos complejos
numeroComplejo = 5 + 5j
print(numeroComplejo, type(numeroComplejo))

